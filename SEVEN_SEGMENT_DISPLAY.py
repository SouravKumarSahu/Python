## Seven Segment Display Function for any nummber ##

def sevenseg(numr):
    disp = { 1:['####',' ## ','### ','####','#  #','####',' ###','####','####','####'] 
            ,2:['#  #','  # ','   #','   #','#  #','#   ','#   ','   #','#  #','#  #'] 
            ,3:['#  #','  # ','####','####','####','####','####','  # ','####','####'] 
            ,4:['#  #','  # ','#   ','   #','   #','   #','#  #',' #  ','#  #','   #'] 
            ,5:['####',' ###','####','####','   #','### ','####',' #  ','####','### '] 
            }
    
    for x in range(1,6):
        row = ""
        for c in str(numr):
            c = int(c)
            row += disp[x][c] + "  "
        print(row,end='\n')
        
        
        
###########################################        
sevenseg(123)
sevenseg(9081726354)
